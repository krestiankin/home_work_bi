'''5. Сыграйте в игру Fizz-Buzz: выведите все числа от 1 до 100; если число делится на 3, вместо числа выведите «fizz».
Если оно делится на 5, вместо числа выведите “buzz”. Если оно делится на оба, выведите «fizz buzz» вместо числа.'''

for _ in range(1, 100):
    if _ % 3 == 0 and _ % 5 == 0:             # перевірка ділення на 3 та 5 
        print('«fizz buzz»')
    elif _ % 5 == 0:                          # перевірка ділення на 5
        print('“buzz”')
    elif _ % 3 == 0:                          # перевірка ділення на 3
        print('«fizz»')
    else:
        print(_)                              # вивід просто числа з циклу



'''6. Сыграйте в игру 7-boom: выведите все числа от 1 до 100; если число делится на 7 или содержит цифру 7, выведите “BOOM” вместо числа.'''

for _ in range(1, 100):
    if _ % 7 == 0:                           # перевірка ділення на 7
        print('“BOOM”')
    elif str(_).find('7') != -1:             # перевірка наявності 7
        print('“BOOM”')
    else:
        print(_)                             # вивід просто числа з циклу


