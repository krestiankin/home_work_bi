''' 1. В качестве входящих данных запросите целое число. Если оно делится на 3, выведите «foo»; если оно делится на 5,
выведите «bar»; если оно делится на оба, выведите «ham» (а не «foo» или «bar»).'''

n = int(input('Введіть ціле число:'))     # інпут та перетворення в ціле число

if n % 3 == 0 and n % 5 == 0:             # перевірка ділення на 3 та 5 
    print('ham')
elif n % 5 == 0:                          # перевірка ділення на 5
    print('bar')
elif n % 3 == 0:                          # перевірка ділення на 3
    print('foo')



'''2. В качестве входящих данных запросите два числа и выведите какое из них меньше и какое большее.''' 

n1, n2 = map(int, input('Введіть ДВА цілих числа через ПРОБІЛ: ').split())                # зчитуємо 2 числа через пробіл
print(f' min: {min(n1, n2)} \n max: {max(n1, n2)}')                                       # вивід та перевірка мін та макс, не грався варіантом з умовами



